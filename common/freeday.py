# works only with years 2000-2030
# doesn't check if date is saturday or sunday
#
def isFreeday(date):

    yyyy = date[:4]
    mm_dd = date[5:]

    if mm_dd == '01-06' and yyyy < '2010':
        return False

    if mm_dd in FREEDAYS:
        return True

    if date in MOVING_FREEDAYS:
        return True

    return False
